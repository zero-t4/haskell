{-|
  Реализуйте функцию трех аргументов lenVec3, которая вычисляет
  длину трехмерного вектора. Аргументы функции задают декартовы
  координаты конца вектора, его начало подразумевается находящимся в
  начале координат. Для извлечения квадратного корня воспользуйтесь
  функцией sqrt, определенной в стандартной библиотеке.

  GHCi> lenVec3 2 3 6
  7.0
-}

lenVec3 x y z =  sqrt $ x ^ 2 + y ^ 2 + z ^ 2

lenVec3' x y z =  sqrt $ sum $ map (^2) [x, y, z]
